COPY supplier FROM 'dbgen/s10/supplier.tbl' WITH (HEADER false, DELIMITER '|');
COPY part FROM 'dbgen/s10/part.tbl' WITH (HEADER false, DELIMITER '|');
COPY customer FROM 'dbgen/s10/customer.tbl' WITH (HEADER false, DELIMITER '|');
COPY partsupp FROM 'dbgen/s10/partsupp.tbl' WITH (HEADER false, DELIMITER '|');
COPY nation FROM 'dbgen/s10/nation.tbl' WITH (HEADER false, DELIMITER '|');
COPY region FROM 'dbgen/s10/region.tbl' WITH (HEADER false, DELIMITER '|');
